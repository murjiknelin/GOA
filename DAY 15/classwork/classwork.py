# 1. Create an if-else statement to check if the temperature exceeds 30 degrees. If it does, print "It's hot outside!"; otherwise, print "It's not too hot."

# 2. Create an if-else statement to determine if a person is a teenager. If the age is less than 19 print "You are a teenager!"; otherwise, print "You are not a teenager."


#1
temperature = input()

if temperature > 30 :
    print("It's hot outside!")
else :
    print("It's not too hot.")

#2
age = input()

if age <= 19:
    print("You are a teenager!")
else :
    print("You are not a teenager.")